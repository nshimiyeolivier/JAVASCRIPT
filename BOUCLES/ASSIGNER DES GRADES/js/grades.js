for (a = 0; a <= 100; a++) {
    if (a >= 90){
        console.log( a + ": A");
    }else if ( a >= 80 && a <= 90){
        console.log( a + ": B")
    }else if (a >= 70 && a <= 80){
        console.log( a + ": C")
    }else if ( a >= 65 && a <= 70){
        console.log( a + ": D")
    } else {
        console.log( a + ": F")
    }
}